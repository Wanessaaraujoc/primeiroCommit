package minhaPrimeiraCalculadora;

public class EngrenagemDaCalculadora {
	
	
	public void soma(int n1 , int n2) {
		int soma = n1 + n2;
		System.out.println(soma);
		
	}
	
	public void subtração(int n1 , int n2) {
		int subtração = n1 - n2;
		System.out.println(subtração);
		
	}
	
	public void divisão(int n1 , int n2) {
		int divisão = n1 / n2;
		System.out.println(divisão);
		
	}
	
	public void multiplicação(int n1 , int n2) {
		int multiplicação = n1 * n2;
		System.out.println(multiplicação);
	}
	

}
